"""
    内存图
        列表嵌套
        浅拷贝
        深拷贝

    拷贝:将数据由一份复制为多份,数据改变互不影响.
    浅拷贝:
        优点:占用内存较少
        缺点:修改深层数据,互相影响
        适用性:优先,没有深层数据或者深层数据不会被修改
    深拷贝:
        优点:一份数据发生变化,另外一份数据绝对不受影响
        缺点:占用内存过多
        适用性:深层数据会被修改
"""
list01 = [10, [20, 30]]
list02 = list01[:] # 浅拷贝
list02[0] = 100
list02[1][1] = 300
print(list01) # ?


import copy

list01 = [10, [20, 30]]
list02 = copy.deepcopy(list01)
list02[0] = 100
list02[1][1] = 300
print(list01) # ?
