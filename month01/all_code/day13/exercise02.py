"""
    练习：说出图形管理器中，设计原则的体现.
        参照demo04,对照exercise01
    　
    六大设计原则:
        开闭原则:能增加三角形等具体图形,
            但是不用修改图形管理器.
        单一职责:
            图形管理器负责统一管理所有图形
            圆形类只负责实现圆形面积计算方式
            矩形类只负责实现矩形面积计算方式
            之间互不影响
        依赖倒置:
            图形管理器使用图形,不使用圆形／矩形
        组合复用:
            组合在连接变化
            继承在统一变化
            例如：
                图形管理器与各种图形是组合关系
                图形类与圆形／矩形类是继承关系
        里氏替换:
            (1)形参是父类型,实参可以是任何子对象
            例如:
                向图形管理器添加的圆形／矩形是对象
            (2)建议子类使用扩展重写
                圆形重写图形计算面积方法时,先通过super()调用父类计算面积方法
        迪米特：
            低耦合(类和类之间的关系松散)
            图形类隔离了图形管理器与具体图形
"""